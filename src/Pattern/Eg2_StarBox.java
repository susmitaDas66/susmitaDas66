package Pattern;

public class Eg2_StarBox {

	public static void main(String[] args)
	{
       //*****
	   //*****
	   //*****
	   //*****

		// Rows = 4
		// Columns = 5
		// Always Starts with Rows(Outer Loop)
		
		for(int i=1;i<=4;i++)
		{
			for(int j=1;j<=5;j++)
			{
				System.out.print("*");
			}
		 System.out.println();
		}
	}

}
